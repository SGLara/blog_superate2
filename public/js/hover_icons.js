function setHoverFacebook(){
    var icon = document.getElementById('facebook');
    var src = icon.getAttribute('src');
    var attrib = "img/index/social_icons/facebook.svg";

    if (src == attrib) {
        icon.setAttribute("src","img/index/social_icons/facebook(1).svg");
    }
}

function setOriginalFacebook(){
    var icon = document.getElementById('facebook');
    icon.setAttribute("src","img/index/social_icons/facebook.svg");
}
